import java.util.Scanner;

public class StringExercise {
    public static void main(String[] args) {
       String value1 = "We don't need no education\nWe don't need no thought control";
       String value2  = "Check \"this\" out!, \"s inside of \"s!";
       String value3 = "In windows, the main drive is usually C:\\";
       String value4 = "I can do backslashes \\, double backslashes \\\\,\n" +
               "and the amazing triple backslash \\\\\\!";
       System.out.print(value1 + "\n" + value2 + "\n" + value3 + "\n" + value4);
    }
}
